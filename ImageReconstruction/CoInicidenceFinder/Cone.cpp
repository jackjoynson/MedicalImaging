#include "Cone.h"

Cone::Cone(double startX, double startY, double startZ, double directionX, double directionY, double directionZ, double halfAngle)
{
	SetStartX(startX);
	SetStartY(startY);
	SetStartZ(startZ);
	SetDirectionX(directionX);
	SetDirectionY(directionY);
	SetDirectionZ(directionZ);
	SetHalfAngle(halfAngle);
}
